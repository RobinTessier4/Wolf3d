#include <libft.h>

void	ft_putnbr(int n)
{
	if (n < 0 && n >= -2147483647)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n >= 0 && n < 10)
		ft_putchar(n + 48);
	else if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	if (n < -2147483647)
	{
		ft_putchar('-');
		ft_putchar('2');
		ft_putnbr(147483648);
	}
}
