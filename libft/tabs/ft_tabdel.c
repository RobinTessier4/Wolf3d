#include <stdlib.h>

void		ft_tabdel(char ***tab)
{
	int		i;
	char	*tmp;

	if (*tab)
	{
		i = 0;
		while ((*tab)[i])
		{
			tmp = (*tab)[i];
			free(tmp);
			i++;
		}
		free(*tab);
		*tab = NULL;
	}
}
